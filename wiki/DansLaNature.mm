Une liste des logiciels et site web utilisant CouchDB, merci d'ajouter le votre !

== Logiciels ==

 * [[http://www.ajatus.info/|Ajatus]]
 * [[http://www.haskell.org/haskellwiki/Yhc_web_service|Yhc Web Service]], compile en ligne du Haskell en Javascript, par Dmitry Golubovsky
 * [[http://www.mockingeye.com/index.php/2008/04/22/introducing-gargoyle-poi-server/|Gargoyle POI Server]], Simple accès REST au points et lieux d'interêts (POI) géographiques dans différents formats (KML, GeoRSS, etc.)
 * [[http://code.google.com/p/erlysocial/|Erlysocial est un robot d'indexation distribué des métadonnées des réseaux sociaux]]
 * [[http://github.com/harryf/urldammit/tree/master|urldammit]], service RESTful "lost+found" pour traquer les urls d'un site. Supporte plusieurs backend (actuelement couchdb et mysql pour ceux qui  n'aime pas les risques)

