The following mailing lists relate to the Apache CouchDB project. Users should consider subscribing to the Users mailing lists. The other lists are for people interested in helping with the development of CouchDB itself.

Formatted archives are available via the [[http://mail-archives.apache.org/mod_mbox/|Apache Mail Archives]] and also via [[http://couchdb.markmail.org/|MarkMail]].


== User Support and Discussion (user) ==

This is a general discussion and support list for CouchDB users. It is the primary source of free CouchDB support. It is also a place for general discussion of topics related to CouchDB.

 * To '''subscribe''', send an email to `<user-subscribe@couchdb.apache.org>`.
 * To '''unsubscribe''', send empty email to `<user-unsubscribe@couchdb.apache.org>`.
 * Finally, to post a message to the list use the address `<user@couchdb.apache.org>`.

The archives for this list can also be [[http://mail-archives.apache.org/mod_mbox/couchdb-user/|browsed online]].

The pre-incubation archives [[http://mail-archives.apache.org/mod_mbox/incubator-couchdb-user/|are available]], too.

== Development Discussion (dev) ==

This is the list where the design and implementation of CouchDB is discussed. This includes current and future features, release schedules, and simply developer discussion of various related topics. All CouchDB committers should be subscribed to this list. Others are welcomed to subscribe and participate if they are interested following development.

 * To '''subscribe''', send an email to `<dev-subscribe@couchdb.apache.org>`.
 * To '''unsubscribe''', send empty email to `<dev-unsubscribe@couchdb.apache.org>`.
 * Finally, to post a message to the list use the address `<dev@couchdb.apache.org>`.

The archives for this list can also be [[http://mail-archives.apache.org/mod_mbox/couchdb-dev/|browsed online]].

The pre-incubation archives [[http://mail-archives.apache.org/mod_mbox/incubator-couchdb-dev/|are available]], too.

== Source Change Reports (commits) ==

This is a read-only list that receives all of the SVN commit messages for CouchDB. All CouchDB committers should be subscribed to this list to be able to see and review changes made by others.

 * To '''subscribe''', send an email to `<commits-subscribe@couchdb.apache.org>`.
 * To '''unsubscribe''', send empty email to `<commits-unsubscribe@couchdb.apache.org>`.

The archives for this list can also be [[http://mail-archives.apache.org/mod_mbox/couchdb-commits/|browsed online]]. 

Replies to commit mails should be directed to the ''dev'' mailing list.
