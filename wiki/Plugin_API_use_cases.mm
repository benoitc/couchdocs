There are plans for CouchDB to support a plugin system, which will support existing pieces of the infrastructure, such as the _view and _search interfaces, as well as other functionality people might want to add in the future, that falls outside the core priorities of CouchDB.

We are gathering requirements for the range of features plugins might add, so please be creative (but realistic) in adding pages about plugin types. 


